Cypress.Commands.add('waitForApi', () => {
    cy.log('Waiting for API...');
    cy.request({
        url: 'http://localhost:8080/api/health', 
        failOnStatusCode: false
    }).then((resp) => {
        if (resp.status !== 200) {
            cy.wait(1000);   
            cy.waitForApi();
        }
    });
});